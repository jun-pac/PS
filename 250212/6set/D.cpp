void Solve(){
    ll l,r,R;
    cin>>R;
    // 일단 정수연산을 해야 함.
    ll R2=2*R;
    // 모두 2배씩 해서 생각해야 할듯.
    ll res=0;
    
    for(ll i=1; i<=R-1; i++){
        l=1, r=R;
        while(l<r){
            ll mid=(l+r+1)/2;
            if((2*mid-1)*(2*mid-1)+(2*i+1)*(2*i+1) > R2*R2) r=mid-1;
            else l=mid;
        }
        res+=l;
    }
    cout<<res*4+1<<'\n';
}

int main(){
    ios_base::sync_with_stdio(false); cin.tie(NULL);
    int t=1;
    // cin>>t;
    while(t--){
        Solve();
    }
    return 0;
}

